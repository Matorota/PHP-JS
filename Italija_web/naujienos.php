<h1 >Coronavirus: How do Italy's rules change in January?</h1>
<img src="naujienos/751dd6e76f383c34fe5e34c029d6be99e6204ec4a3e7f58dfe333373a53debd4.jpg" alt="Girl in a jacket" class="pagalba">
<p>The Italian government had said in early December that the country could begin to reopen from January 7th, which is when the current set of rules expires.
</p>


<p>But as new cases of coronavirus remain higher than hoped, that isn't happening.
</p>


<p>The Italian government has approved extensions to many rules from January 7th. Here's what we know so far.
</p>


<h1>What are the rules from January 7th?</h1>

<p>Many of Italy's current restrictions stay in place and planned reopenings have been postponed, according to a government ordinance approved in the early hours of Tuesday morning.</p>



<h1>What about travel?</h1>

</p>Non-essential travel between regions is to remain prohibited until at least January 15th.</p>

<p>International travellers who spent any part of the period from December 21st to January 6th outside Italy must continue to quarantine for 14 days after their arrival in Italy, even if they're entering Italy after the period itself. Find more information here.</p>


<p>People who leave and return to Italy after this time – i.e., people who spent the entire period between December 21st and January 6th in Italy and are only travelling from January 7th onwards – can show a negative Covid-19 test from the past 48 hours to avoid quarantining on arrival.</p>

